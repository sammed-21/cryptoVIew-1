import mongoose, { model } from "mongoose";

const { Schema } = mongoose;

const WorkoutSchema = new Schema(
  {
    title: {
      type: String,
      required: true,
    },
    reps: {
      type: Number,
      required: true,
    },
    load: {
      type: Number,
      required: true,
    },
  },
  { timestamps: true }
);

export default model("Workout", WorkoutSchema);
